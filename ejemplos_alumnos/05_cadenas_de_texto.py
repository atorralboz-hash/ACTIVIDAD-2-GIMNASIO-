# Diferentes definiciones
cadena_simple = ' Hola mundo! '
cadena_doble = " ¡Hola, mundo!"
cadena_triple = """ ejemplo de cadena con muchas líneas. """
print(cadena_simple)
print(cadena_doble)
print(cadena_triple)

# Concatenacion
saludo = "hello"
nombre = "alejandra"
print(saludo + " " + nombre)

# comversion
edad = 33
print("soy " + nombre + " " + str(edad))

# f-string
print (f"hola, me llamo {nombre} y tengo {edad} años")



